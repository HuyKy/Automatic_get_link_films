def PathFile():
	return os.getcwd()

def FileTXT(namefile):
	try:
		with open(namefile,mode='r+') as fjob:
			data = fjob.readlines()
			fjob.seek(0)
		return data
	except:
		with open(f"{namefile}.txt",mode='r+') as fjob:
			data = fjob.readlines()
			fjob.seek(0)
		return data

def Handle(raws,Choice,namefile):
	dict_handle = dict()
	ep = 1
	pattern = re.compile('10000000_[0-9]+_[0-9]+_n.mp4')
	if Choice == 'y':
		for raw in raws:
			try:
				handle = pattern.findall(raw)
				dict_handle[ep] = handle[0]
				ep+=1
			except:
				pass
	else:
		with open(namefile,mode='r+') as fjob:
			for raw in raws:
				try:
					handle = pattern.findall(raw)
					dict_handle[ep] = handle[0]

					ep+=1
				except:
					pass
	yield dict_handle
	yield ep

